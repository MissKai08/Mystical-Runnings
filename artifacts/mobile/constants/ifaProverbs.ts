export function getDailyProverb(date: Date): IfaProverb {
  const start = new Date(2024, 0, 1).getTime();
  const dayIndex = Math.floor((date.getTime() - start) / (1000 * 60 * 60 * 24));
  return IFA_PROVERBS[((dayIndex % IFA_PROVERBS.length) + IFA_PROVERBS.length) % IFA_PROVERBS.length];
}
